import UIKit

extension UIButton {
    
    func setSelectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.4922490716, green: 0.7722237706, blue: 0.4631416798, alpha: 1)
    }
    
    func setDeselectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.7464068532, green: 0.8857156038, blue: 0.7402073741, alpha: 1)

    }
}
